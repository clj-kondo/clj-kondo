(defn class->file* [class]
  (let [parts (str/split class #"\.")
        parts* (butlast parts)
        file (last parts)
        file (str file ".java")]
    (concat parts* [file])))

(defn class->file [class]
  (some
   (fn [base]
     (let [f (apply io/file base
                   (class->file* class))]
       (when (.exists f) f)))
   base-dirs))

